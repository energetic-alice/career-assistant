/**
 * Детерминированная зачистка форматирования для русскоязычных текстов,
 * выходящих наружу (финальный анализ клиента) и для исходников промптов
 * (market-data-таблицы, kb-файлы), которые подгружаются в LLM.
 *
 * Что делает:
 *   - "ё" / "Ё" → "е" / "Е"
 *   - "—" (em dash, U+2014) → "-" (минус U+002D)
 *   - "–" (en dash, U+2013) → "-" (минус U+002D)
 *
 * Зачем:
 *   1. Style-guide и 04b-cleanup эти правила декларируют, но Claude
 *      их регулярно нарушает (особенно длинное тире — оно есть в каждой
 *      второй фразе обучающей выборки). Финальный детерминированный
 *      проход даёт 100%-гарантию.
 *   2. Этот же sanitize вызывается из probe-uk-market / probe-ru-market
 *      при записи market-data файлов, чтобы Claude не учился стилю на
 *      нашем же образце.
 *
 * НЕ задевает:
 *   - HTML-таблицы внутри финала (там нет ё/тире);
 *   - английский текст (em-dash в английском допустим — но мы работаем
 *     только с русскоязычными выходами; для англо-документов
 *     `runAnalysisPhase4` `sanitize` не вызывает).
 */
export function sanitizeRussianText(input: string): string {
  return input
    .replace(/ё/g, "е")
    .replace(/Ё/g, "Е")
    .replace(/—/g, "-")
    .replace(/–/g, "-");
}
